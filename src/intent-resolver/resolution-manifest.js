/**
 * ═══════════════════════════════════════════════════════════
 *  LUME RESOLUTION MANIFEST — Deterministic Compilation
 *  CHI Paper §8.2 — The Resolution Manifest (lume-lock.json)
 *
 *  Records every natural-language-to-code mapping during
 *  compilation. On subsequent compilations, the manifest is
 *  checked FIRST — ensuring 100% reproducible builds even
 *  if underlying NLP models change.
 *
 *  Each entry contains:
 *    - source: the original English input
 *    - resolvedTo: the AST node type produced
 *    - resolvedBy: which Tolerance Chain layer resolved it
 *    - confidence: the resolution confidence (0.0–1.0)
 *    - astHash: SHA-256 of the AST node
 *    - outputHash: SHA-256 of the generated JavaScript
 *    - timestamp: when the resolution was recorded
 *
 *  Usage:
 *    const manifest = new ResolutionManifest('.lume/lume-lock.json')
 *    const cached = manifest.lookup('get all the users')
 *    if (cached) { use cached.ast } else { run tolerance chain }
 *    manifest.record(source, ast, resolvedBy, confidence)
 *    manifest.save()
 * ═══════════════════════════════════════════════════════════
 */

import { createHash } from 'crypto'
import fs from 'fs'
import path from 'path'

const MANIFEST_FILENAME = 'lume-lock.json'
const MANIFEST_VERSION = 1

export class ResolutionManifest {
    constructor(projectRoot = '.') {
        this.projectRoot = projectRoot
        this.manifestPath = path.join(projectRoot, '.lume', MANIFEST_FILENAME)
        this.entries = new Map()
        this.metadata = {
            version: MANIFEST_VERSION,
            created: new Date().toISOString(),
            lastCompiled: null,
            compilerVersion: '0.9.0',
            totalResolutions: 0,
            layerDistribution: {},
        }
        this._load()
    }

    /**
     * Load existing manifest from disk
     */
    _load() {
        try {
            if (fs.existsSync(this.manifestPath)) {
                const raw = JSON.parse(fs.readFileSync(this.manifestPath, 'utf-8'))
                this.metadata = raw.metadata || this.metadata
                if (raw.entries && Array.isArray(raw.entries)) {
                    for (const entry of raw.entries) {
                        this.entries.set(entry.sourceHash, entry)
                    }
                }
            }
        } catch (e) {
            console.warn(`[manifest] Could not load ${this.manifestPath}:`, e.message)
        }
    }

    /**
     * Save manifest to disk
     */
    save() {
        try {
            const lumeDir = path.join(this.projectRoot, '.lume')
            if (!fs.existsSync(lumeDir)) {
                fs.mkdirSync(lumeDir, { recursive: true })
            }
            this.metadata.lastCompiled = new Date().toISOString()
            this.metadata.totalResolutions = this.entries.size

            // Compute layer distribution
            const dist = {}
            for (const entry of this.entries.values()) {
                dist[entry.resolvedBy] = (dist[entry.resolvedBy] || 0) + 1
            }
            this.metadata.layerDistribution = dist

            const output = {
                _comment: 'LUME RESOLUTION MANIFEST — DO NOT EDIT. Generated by the Lume compiler.',
                metadata: this.metadata,
                entries: [...this.entries.values()],
            }
            fs.writeFileSync(this.manifestPath, JSON.stringify(output, null, 2))
        } catch (e) {
            console.warn(`[manifest] Could not save manifest:`, e.message)
        }
    }

    /**
     * Look up a cached resolution by source text.
     * Returns the cached entry or null if not found.
     *
     * @param {string} source - The English source line
     * @returns {object|null} Cached entry with ast, resolvedBy, confidence
     */
    lookup(source) {
        const hash = this._hashSource(source)
        const entry = this.entries.get(hash)
        if (!entry) return null

        // Verify the entry hasn't been tampered with
        if (entry.integrityHash) {
            const expected = this._computeIntegrity(entry)
            if (expected !== entry.integrityHash) {
                console.warn(`[manifest] Integrity check failed for: "${source}"`)
                this.entries.delete(hash)
                return null
            }
        }

        return {
            ast: entry.ast,
            resolvedBy: 'manifest_cache',
            originalResolvedBy: entry.resolvedBy,
            confidence: entry.confidence,
            cached: true,
            astHash: entry.astHash,
        }
    }

    /**
     * Record a new resolution in the manifest.
     *
     * @param {string} source - The original English input
     * @param {object} ast - The resolved AST node
     * @param {string} resolvedBy - Which Tolerance Chain layer resolved it
     * @param {number} confidence - Resolution confidence (0.0–1.0)
     * @param {string} [jsOutput] - Optional: the generated JavaScript output
     */
    record(source, ast, resolvedBy, confidence, jsOutput = null) {
        const sourceHash = this._hashSource(source)
        const astHash = this._hashObject(ast)
        const outputHash = jsOutput ? this._hashString(jsOutput) : null

        const entry = {
            source: source.trim(),
            sourceHash,
            resolvedTo: ast.type || 'Unknown',
            resolvedBy,
            confidence: Math.round(confidence * 1000) / 1000,
            astHash,
            outputHash,
            ast: this._compactAST(ast),
            timestamp: new Date().toISOString(),
        }

        // Add integrity hash (tamper detection)
        entry.integrityHash = this._computeIntegrity(entry)

        this.entries.set(sourceHash, entry)
    }

    /**
     * Verify the entire manifest's integrity
     * @returns {{ valid: boolean, errors: string[] }}
     */
    verify() {
        const errors = []
        for (const [hash, entry] of this.entries) {
            // Verify source hash
            const expectedHash = this._hashSource(entry.source)
            if (expectedHash !== hash) {
                errors.push(`Source hash mismatch for: "${entry.source}"`)
            }

            // Verify integrity hash
            if (entry.integrityHash) {
                const expectedIntegrity = this._computeIntegrity(entry)
                if (expectedIntegrity !== entry.integrityHash) {
                    errors.push(`Integrity hash mismatch for: "${entry.source}"`)
                }
            }
        }

        return { valid: errors.length === 0, errors, totalEntries: this.entries.size }
    }

    /**
     * Get statistics about the manifest
     */
    getStats() {
        const layers = {}
        let totalConfidence = 0
        for (const entry of this.entries.values()) {
            layers[entry.resolvedBy] = (layers[entry.resolvedBy] || 0) + 1
            totalConfidence += entry.confidence
        }

        return {
            totalEntries: this.entries.size,
            layerDistribution: layers,
            averageConfidence: this.entries.size > 0
                ? Math.round((totalConfidence / this.entries.size) * 100) / 100
                : 0,
            manifestPath: this.manifestPath,
            lastCompiled: this.metadata.lastCompiled,
        }
    }

    /**
     * Clear the manifest (for fresh compilation)
     */
    clear() {
        this.entries.clear()
    }

    /* ── Hashing utilities ──────────────────────────────── */

    _hashSource(source) {
        return this._hashString(source.toLowerCase().trim().replace(/\s+/g, ' '))
    }

    _hashString(str) {
        return createHash('sha256').update(str).digest('hex').substring(0, 16)
    }

    _hashObject(obj) {
        const cleaned = JSON.parse(JSON.stringify(obj, (key, val) => {
            // Strip non-deterministic fields
            if (['line', 'timestamp', 'resolvedBy'].includes(key)) return undefined
            return val
        }))
        return this._hashString(JSON.stringify(cleaned))
    }

    _computeIntegrity(entry) {
        const data = `${entry.source}|${entry.resolvedTo}|${entry.resolvedBy}|${entry.confidence}|${entry.astHash}`
        return this._hashString(data)
    }

    /**
     * Strip bulky/non-essential fields from AST for compact storage
     */
    _compactAST(ast) {
        const compact = { ...ast }
        delete compact.line
        delete compact.resolvedBy
        delete compact.similarity
        delete compact.language
        delete compact.hints
        return compact
    }
}

/**
 * CLI: lume verify --manifest
 * Verifies the integrity of a lume-lock.json file
 */
export function verifyManifestCLI(projectRoot = '.') {
    const manifest = new ResolutionManifest(projectRoot)
    const result = manifest.verify()
    const stats = manifest.getStats()

    if (result.valid) {
        console.log(`✓ Manifest verified: ${result.totalEntries} entries, all hashes valid.`)
        console.log(`  Average confidence: ${stats.averageConfidence}`)
        console.log(`  Layer distribution:`)
        for (const [layer, count] of Object.entries(stats.layerDistribution)) {
            console.log(`    ${layer}: ${count}`)
        }
    } else {
        console.error(`✗ Manifest verification FAILED:`)
        for (const error of result.errors) {
            console.error(`  - ${error}`)
        }
    }

    return result
}
