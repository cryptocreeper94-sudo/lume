import { describe, it } from 'node:test'
import assert from 'node:assert/strict'
import { tokenize } from '../../src/lexer.js'
import { Parser } from '../../src/parser.js'
import { Transpiler } from '../../src/transpiler.js'

// Helper — takes Lume code, returns compiled JavaScript
function compile(code) {
    const tokens = tokenize(code)
    const parser = new Parser(tokens, '<test>')
    const ast = parser.parse()
    const transpiler = new Transpiler(ast, '<test>')
    return transpiler.transpile()
}

// ══════════════════════════════════════
//  Header
// ══════════════════════════════════════

describe('Transpiler: Header', () => {
    it('includes "Generated by Lume Compiler" header', () => {
        const js = compile('let x = 1')
        assert.ok(js.includes('Generated by Lume Compiler'))
    })
    it('output is a string', () => {
        const js = compile('let x = 1')
        assert.equal(typeof js, 'string')
    })
    it('ends with newline', () => {
        const js = compile('let x = 1')
        assert.ok(js.endsWith('\n'))
    })
})

