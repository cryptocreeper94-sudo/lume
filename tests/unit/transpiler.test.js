// ══════════════════════════════════════
//  Integration: Full Pipeline
// ══════════════════════════════════════

describe('Transpiler: Full Pipeline Integration', () => {
    it('compiles a complete program', () => {
        const code = `let name = "Alice"
let count = 0
show "Hello, {name}!"
if count is 0:
    show "empty"`
        const js = compile(code)
        assert.ok(js.includes('let name = "Alice"'))
        assert.ok(js.includes('let count = 0'))
        assert.ok(js.includes('console.log'))
        assert.ok(js.includes('if'))
    })
    it('compiles function with loop', () => {
        const code = `to sum_list(items):
    let total = 0
    for each item in items:
        set total to total
    return total`
        const js = compile(code)
        assert.ok(js.includes('function sum_list(items)'))
        assert.ok(js.includes('for (const item of items)'))
        assert.ok(js.includes('return total'))
    })
    it('compiles type + function', () => {
        const code = `type Point:
    x: number
    y: number

to distance(p):
    return p.x`
        const js = compile(code)
        assert.ok(js.includes('class Point'))
        assert.ok(js.includes('function distance(p)'))
    })
})
