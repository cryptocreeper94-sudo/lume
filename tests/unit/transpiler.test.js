describe('Transpiler: IfStatement', () => {
    it('emits if block', () => {
        const js = compile('if true:\n    show "yes"')
        assert.ok(js.includes('if (true)'))
        assert.ok(js.includes('{'))
        assert.ok(js.includes('}'))
    })
    it('emits if/else', () => {
        const js = compile('if true:\n    show "a"\nelse:\n    show "b"')
        assert.ok(js.includes('} else {'))
    })
    it('emits else if chain', () => {
        const js = compile('if x is 1:\n    show "a"\nelse if x is 2:\n    show "b"')
        assert.ok(js.includes('else if'))
    })
    it('compiles is to ==', () => {
        const js = compile('if x is 1:\n    show "yes"')
        assert.ok(js.includes('=='))
    })
    it('compiles is not to !=', () => {
        const js = compile('if x is not null:\n    show "exists"')
        assert.ok(js.includes('!='))
    })
    it('compiles is greater than to >', () => {
        const js = compile('if x is greater than 5:\n    show "big"')
        assert.ok(js.includes('>'))
    })
    it('compiles is less than to <', () => {
        const js = compile('if x is less than 0:\n    show "neg"')
        assert.ok(js.includes('<'))
    })
    it('compiles is at least to >=', () => {
        const js = compile('if x is at least 10:\n    show "ok"')
        assert.ok(js.includes('>='))
    })
    it('compiles is at most to <=', () => {
        const js = compile('if x is at most 100:\n    show "ok"')
        assert.ok(js.includes('<='))
    })
})

