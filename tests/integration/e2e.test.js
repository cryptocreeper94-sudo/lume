/* ── Helper: compile and run Lume source ── */
function compileAndRun(source, filename = '<test>') {
    const tokens = tokenize(source, filename)
    const ast = parse(tokens, filename)
    const js = transpile(ast, filename)
    const code = js.replace(/^\/\/ Generated by Lume Compiler\n\n?/, '')
    const output = []
    const origLog = console.log
    console.log = (...args) => output.push(args.join(' '))
    try {
        new Function(code)()
    } finally {
        console.log = origLog
    }
    return { js, output }
}

/* ═══ E2E: Core Language Pipeline ═══ */
describe('E2E: Core Language Pipeline', () => {
    it('hello world compiles and runs', () => {
        const { output } = compileAndRun('show "Hello, Lume!"')
        assert.deepStrictEqual(output, ['Hello, Lume!'])
    })

    it('variables and math', () => {
        const { output } = compileAndRun('let x = 10\nlet y = 20\nshow x + y')
        assert.deepStrictEqual(output, ['30'])
    })

    it('string concatenation', () => {
        const { output } = compileAndRun('let name = "Lume"\nshow "Hello, " + name + "!"')
        assert.deepStrictEqual(output, ['Hello, Lume!'])
    })

    it('conditionals', () => {
        const { output } = compileAndRun('let x = 42\nif x > 40:\n    show "big"')
        assert.deepStrictEqual(output, ['big'])
    })

    it('show statement compiles to console.log', () => {
        const { js } = compileAndRun('show "Lume works"')
        assert.ok(js.includes('console.log'), 'show should compile to console.log')
    })
})

/* ═══ E2E: English Mode Pipeline ═══ */
describe('E2E: English Mode Resolution', () => {
    it('resolves basic English to AST nodes', async () => {
        const result = await resolveEnglishFile('mode: english\n\nget the user name\nshow the user name', { strict: false })
        assert.ok(result.ast.length >= 2, 'Should produce at least 2 AST nodes')
        const types = result.ast.map(n => n.type)
        assert.ok(types.includes('VariableAccess'), 'Should include VariableAccess')
        assert.ok(types.includes('ShowStatement'), 'Should include ShowStatement')
    })

    it('resolves CRUD operations', async () => {
        const result = await resolveEnglishFile('mode: english\n\ncreate a new task with title\nsave the task to the database\ndelete the old tasks', { strict: false })
        const types = result.ast.map(n => n.type)
        assert.ok(types.includes('CreateOperation'))
        assert.ok(types.includes('StoreOperation'))
        assert.ok(types.includes('DeleteOperation'))
    })

    it('resolves loops', async () => {
        const result = await resolveEnglishFile('mode: english\n\nrepeat 5 times\nfor each item in the list', { strict: false })
        const types = result.ast.map(n => n.type)
        assert.ok(types.includes('RepeatLoop'))
        assert.ok(types.includes('ForEachLoop'))
    })

    it('resolves math operations', async () => {
        const result = await resolveEnglishFile('mode: english\n\nadd 10 to the price\nsubtract 5 from the total', { strict: false })
        const types = result.ast.map(n => n.type)
        assert.ok(types.includes('BinaryExpression'))
    })

    it('tracks resolution stats', async () => {
        const result = await resolveEnglishFile('mode: english\n\n# a comment\nget the data\nshow the result', { strict: false })
        assert.ok(result.stats, 'Should have stats object')
        assert.ok(typeof result.stats.resolvedLines === 'number', 'Should track resolved count')
    })
})

