/* ═══ E2E: lume create ═══ */
describe('E2E: lume create', () => {
    it('parses app description into plan', () => {
        const result = parseAppDescription('a blog with comments and search')
        assert.ok(result, 'Should return a result object')
        assert.ok(result.plan, 'Should have a plan')
        assert.ok(result.plan.components.length > 0, 'Should have components')
        assert.ok(Object.keys(result.plan.models).length > 0, 'Should have models')
    })

    it('generates files with real content', () => {
        const result = parseAppDescription('an ecommerce store with products')
        const { files } = generateProjectStructure(result.plan)
        assert.ok(files.length >= 5, 'Should generate at least 5 files')

        // Check key files exist and have content
        const pkgJson = files.find(f => f.path === 'package.json')
        assert.ok(pkgJson, 'package.json should exist')
        assert.ok(pkgJson.content.includes('"name"'), 'package.json should have real content')

        const indexHtml = files.find(f => f.path === 'index.html')
        assert.ok(indexHtml, 'index.html should exist')
        assert.ok(indexHtml.content.includes('<!DOCTYPE html>'), 'index.html should have doctype')

        const mainJs = files.find(f => f.path === 'src/main.js')
        assert.ok(mainJs, 'main.js should exist')
        assert.ok(mainJs.content.includes('import'), 'main.js should have imports')
    })
})

